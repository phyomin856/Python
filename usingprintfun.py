##### Using print function by using format method and format string #####

name = "Phyo Min Khant"
job = "Student"

##### Format Method #####
print("My name is {} and I am a {}".format(name,job))
print("My name is {1} and I am a {0}".format(name,job))

##### Format String #####
print(f"My name is {name} and I am a {job}.")
